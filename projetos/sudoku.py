def get_list_from_quadrant(matrix, quad_row, quad_col):
    """Retorna uma lista representando um quadrante da matriz."""
    return [
        row[col_index] \
            for row_index, row in enumerate(matrix) \
            for col_index in range(len(matrix)) \
            if row_index // 3 == quad_row and col_index // 3 == quad_col
    ]


